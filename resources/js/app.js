window.sendAjax = function(props) {
  // Default options are marked with *
    return fetch(props.url, {
        method: props.method || 'GET', // *GET, POST, PUT, DELETE, etc.
        mode: "no-cors", // no-cors, cors, *same-origin
        cache: "no-cache", // *default, no-cache, reload, force-cache, only-if-cached
        credentials: "same-origin", // include, *same-origin, omit
        headers: {
            "Content-Type": "application/json; charset=utf-8",
            // "Content-Type": "application/x-www-form-urlencoded",
        },
        redirect: "follow", // manual, *follow, error
        referrer: "no-referrer", // no-referrer, *client
        //body: JSON.stringify(props.data || {}), // body data type must match "Content-Type" header
    })
    .then(response => console.log(response)); // parses response to JSON
}